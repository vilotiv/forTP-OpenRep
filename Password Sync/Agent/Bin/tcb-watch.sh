#!/bin/bash
###############################################################################
#
# Password Sync
# Автор: Легасов В.А.
#
# tcb-watch.sh
#
# Назначение:
#
# Мониторинг каталога TCB.
#
# При обнаружении изменения вызывает create_event.sh
#
###############################################################################

###############################################################################
# Подключение общей библиотеки
###############################################################################

source /opt/password_sync/bin/password_sync_lib.sh

###############################################################################
# Проверяем наличие inotifywait
###############################################################################

command -v inotifywait >/dev/null 2>&1

if [ $? -ne 0 ]; then

    die "Не найден пакет inotify-tools."

fi

###############################################################################
# Проверяем каталог TCB
###############################################################################

check_directory "${TCB_DIR}" || exit 1

###############################################################################
# Проверяем наличие create_event.sh
###############################################################################

CREATE_EVENT_SCRIPT="/opt/password_sync/bin/create_event.sh"

check_file "${CREATE_EVENT_SCRIPT}" || exit 1

###############################################################################
# Запуск службы
###############################################################################

log_info "Служба мониторинга TCB успешно запущена."

###############################################################################
# Начало мониторинга
###############################################################################

inotifywait \
        -m \
        -r \
        -e close_write \
        -e moved_to \
        -e create \
        -e attrib \
        --format "%w %e %f" \
        "${TCB_DIR}" |
while read DIRECTORY EVENT FILE
do

    ###########################################################################
    # Нас интересует только файл shadow
    ###########################################################################

    [ "${FILE}" != "shadow" ] && continue

    ###########################################################################
    # Получаем имя пользователя
    ###########################################################################

    USER_NAME=$(basename "${DIRECTORY}")

    ###########################################################################
    # Проверяем существование пользователя
    ###########################################################################

    check_user "${USER_NAME}" || continue

    ###########################################################################
    # Проверяем ignore-файл
    ###########################################################################

    if [ -f "/run/password_sync.ignore.${USER_NAME}" ]; then

        log_info "Изменение пароля пользователя '${USER_NAME}' выполнено системой синхронизации. Событие пропущено."

        continue

    fi

    ###########################################################################
    # Небольшая задержка
    #
    # Позволяет дождаться окончания всех операций записи.
    ###########################################################################

    sleep 2

    ###########################################################################
    # Запускаем создание события
    ###########################################################################

    "${CREATE_EVENT_SCRIPT}" "${USER_NAME}"

done
